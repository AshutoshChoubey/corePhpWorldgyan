         <li class="PointTutorials">To delete tables in a database instance - TRUNCATE .</li><br />

         <li class="PointTutorials">To rename database instances - RENAME .</li><br />

         </ul>

         

         <p class="h3" align="center">Data Manipulation Language (DML)</p>

         <ul>

         <li class="PointTutorials">To read records from table(s) - SELECT .</li><br />

         <li class="PointTutorials">To insert record(s) into the table(s) - INSERT .</li><br />

         <li class="PointTutorials">Update the data in table(s) - UPDATE .</li><br />

         <li class="PointTutorials">Delete all the records from the table - DELETE .</li><br />

         </ul>

         

