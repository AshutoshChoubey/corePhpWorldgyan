</p><br/>
 

<li class="PointTutorials" >  शाहजहां की स्वयं की अदालत का इतिहास, बादशाहनमा, मानते हैं (पृष्ठ 403, खंड 1) कि एक गौम (इमरत-ए-अलीशान वा गंबेज़) के साथ एक अद्वितीय भव्य भव्य भवन, को मुमताज की दफ़नाने के लिए जयपुर महाराज जयसिंह से लिया गया था, और भवन राजा मानसिंह के महल के रूप में जाना जाता था
</li><br/>
 

<li class="PointTutorials" >  तख्तापलट ने ताजमहल के बाहर वास्तुकला विभाग को शाहजहां द्वारा निर्मित अपनी मूर्ति महल के लिए मकबरे के रूप में वर्णन किया, 1631 से 1653 तक 22 वर्षों से यह पट्टिका ऐतिहासिक बुनने का एक नमूना है। सबसे पहले, फलक साइटें अपने दावे के लिए कोई प्राधिकरण नहीं दूसरा, महिला का नाम मुमताज-उल-ज़मानी था और मुमताजमहल नहीं था। तीसरा, 22 वर्षों की अवधि अविश्वसनीय फ्रांसीसी आगंतुक टैवनरियर द्वारा सभी मुस्लिम संस्करणों को बहिष्कृत करने के लिए कुछ अजीब जंबो से ली गई है, जो एक मूर्खता है।
</li><br/>
 

<li class="PointTutorials" >  राजकुमार औरंगजेब के पत्र (ऊपर चित्रा 1 को देखें) अपने पिता, सम्राट शाहजहां को कम से कम तीन लेखों में दर्ज किया गया है, जिन्हें आदाब-ए-अलामगिरी, यदगर्नामा और मुरुक़ा-ए-अकबरबादी नाम दिया गया है (सईद अहमद, आगरा, 1 9 31 द्वारा संपादित , पृष्ठ 43, फुटनोट 2)। उस पत्र में औरंगजेब के रिकॉर्ड 1652 एडी में ही थे कि मुमताज की फांसी की दफ़नाने वाली कई इमारतें सात मंजिला थीं और इतनी बूढ़ी थीं कि वे सब लीक कर रहे थे, जबकि गुंबद ने उत्तरी दिशा में एक दरार विकसित किया था। इसलिए, औरंगजेब ने अपने खर्चे पर भवनों को तत्काल मरम्मत का आदेश दिया था जबकि सम्राट की सिफारिश करते हुए कि अधिक विस्तृत मरम्मत बाद में की जाए। यह सबूत है कि शाहजहां के शासनकाल के दौरान तत्काल मरम्मत की आवश्यकता के लिए ताज परिसर बहुत पुराना था।
</li><br/>
 

<li class="PointTutorials" >  जयपुर के पूर्व महाराजा ने अपने गुप्त निजी कपडद्वारा संग्रह में 18 दिसंबर, 1633 (शाहजहां के आधुनिक आदेशों आर .176 और 177 के अनुसार) से दो आदेश बनाए हैं, ताज बिल्डिंग कॉम्प्लेक्स का अनुरोध किया है। यह इतना हड़कंप मच गया था कि जयपुर के तत्कालीन शासक दस्तावेज़ को सार्वजनिक करने के लिए शर्मिंदा था।
</li><br/>
 

<li class="PointTutorials" > बीकानेर में राजस्थान राज्य अभिलेखागार शाहजहां द्वारा जयपुर के शासक जयसिंह को संबोधित किए गए तीन अन्य फर्मों को संरक्षित करते हैं, जिन्होंने मकराना क्वीरिस से संगमरमर (मुमताज की कब्र और कोरैनीक ग्राफ्ट के लिए) की आपूर्ति करने के आदेश दिए थे, और पत्थर के कटर थे। जयसिंह जाहिरा तौर पर ताजमहल के जबरदस्त जब्ती पर इतना गुस्से में था कि उन्होंने ताजमहल के अपवित्रता के लिए कोरानिक नक्काशी और नकली कंटोटाफ के लिए संगमरमर का उपयोग करके शाहजहां को उपकृत करने से इनकार कर दिया। जयसिंग ने शाहजहां की संगमरमर और पत्थर के कटर की मांग को देखते हुए, चोटों में अपमानित अपमान के रूप में देखा। इसलिए, उन्होंने किसी संगमरमर को भेजने से इनकार कर दिया और इसके बदले पत्थर के कटर को उसकी सुरक्षात्मक हिरासत में हिरासत में लिया।
</li><br/>
 

<li class="PointTutorials" > . संगमरमर की मांग करने वाली तीन फर्मों को मुमताज की मौत के दो साल के भीतर जयसिंग को भेजा गया। यदि शाहजहां ने वास्तव में 22 साल की अवधि में ताजमहल का निर्माण किया था, तो मुमताज की मौत के तुरंत बाद संगमरमर को केवल 15 या 20 साल बाद ही ज़रूरत पड़ती।
</li><br/>
 

<li class="PointTutorials" > . इसके अलावा, तीनों ने ताजमहाल, न ही मुमताज, न ही दफन नहीं किया। पत्थर की लागत और मात्रा का भी उल्लेख नहीं किया गया है। यह साबित करता है कि ताजमहल के साथ छेड़छाड़ और छेड़छाड़ करने के लिए संगमरमर की एक महत्वपूर्ण मात्रा की आवश्यकता थी। अन्यथा शाहजहां एक गैर-सहकारी जयसिंह पर संगमरमर के लिए घृणास्पद निर्भरता से ताजमहल का निर्माण करने की उम्मीद नहीं कर सकता था।
</li><br/>
 
<p class="paragraph">
यूरोपीय आगंतुक खाता
</p><br/>
 

<li class="PointTutorials" > . एक फ्रांसीसी जौहरी, जो कि एक फ्रांसीसी जौहरी ने अपनी यात्रा के संस्मरणों में दर्ज किया है कि शाहजहां ने जानबूझकर तज़-ए-मकान (यानी 'ताज इमारत') के पास मुमताज को दफनाया है, जहां वे भी आज भी ऐसा करते हैं, ताकि दुनिया प्रशंसित हो सके। । उन्होंने यह भी कहा कि मचान की लागत पूरे काम के मुकाबले ज्यादा थी। शाहजहां ने तेजोमहलय शिव मंदिर में जो काम किया था, उसमें महारानी जुड़नारों पर लूट लिया गया था, शिव की मूर्तियों को उखाड़ फेंकने, दो कहानियों पर उनके स्थान पर केन्द्रोपायों को लगाया गया, कोरियों के साथ कुरान को सम्मिलित करना और सात कहानियों में से छह ताज। यह यह लूट, अपमानजनक और उन कमरों का लुप्तप्राय था जो 22 साल ले गए थे।
</li><br/>
 

<li class="PointTutorials" > . पीटर मुंडे, आगरा के एक अंग्रेजी आगंतुक, 1632 में (मुमताज की मौत के केवल एक वर्ष के भीतर) में दर्ज किया गया था कि 'आगरा के आसपास और आस-पास के नोटों के स्थानों में ताज-ए-महल की कब्र, उद्यान और बाजार शामिल थे।' इसलिए, यह पुष्टि करता है कि ताजमहल शाहजहां से पहले एक उल्लेखनीय इमारत थी।
</li><br/>
 

<li class="PointTutorials" > . डी लाइट, एक डच अधिकारी ने पूर्व शाहजहां के समय का एक उत्कृष्ट भवन के रूप में, आगरा किले से एक मील के बारे में मानसिंह के महल को सूचीबद्ध किया है। शाहजहां की अदालत का इतिहास, बदशाननाम रिकॉर्ड, उसी मानसिंह के महल में मुमताज की दफ़न
</li><br/>
 

<li class="PointTutorials" > . बर्नियर, एक समकालीन फ्रेंच विजिटर ने उल्लेख किया है कि गैर-मुस्लिमों को तहखाने में प्रवेश पर प्रतिबंध लगा दिया गया था (उस समय जब शाहजहां ने मानसिंह के महल की मांग की थी) जिसमें एक चमकदार प्रकाश था। जाहिर है, उसने चांदी के दरवाजे, सोने की रेलिंग, मणि की जड़ें और शिव की मूर्ति पर लटका मोती के तारों को समझाया। शाहजहां ने मुमताज की मौत को एक सह बनाने के लिए, सभी धन प्राप्त करने के लिए इमारत को दोबारा किया
	</li><br/>
<li class="PointTutorials" > . जोहान अल्बर्ट मंडेलस्लो, जो 1638 में मुगलता की मौत के बाद (केवल 7 साल बाद मुमताज की मौत के बाद) में जीवन का वर्णन करते हैं (अपनी यात्राएं और ट्रेवल्स टू वेस्ट-इंडीज में, जॉन स्टार्की और जॉन बास्सेट, लंदन द्वारा प्रकाशित) ताजमहल का निर्माण हो रहा है, हालांकि यह सामान्य रूप से उल्लेखनीय रूप से माना जाता है या माना जाता है कि ताज 1631 से 1653 तक बनाया जा रहा था।
</li><br/>
 
