<li class="PointTutorials" >  उस शाहजहां, जो कि संगमरमर ताज के निर्माण से दूर है, ने इसे केवल काले रंग की छिद्रण के साथ ढंक दिया था, इश्तेदार अमानत खान शिरीज़ी ने स्वयं इमारत पर एक शिलालेख में उल्लेख किया है। कुरानिक अक्षरों की एक छानबीन से पता चलता है कि वे एक प्राचीन शिव मंदिर पर विभिन्न प्रकार के पत्थर के टुकड़े के साथ गेट लगाए गए हैं।
