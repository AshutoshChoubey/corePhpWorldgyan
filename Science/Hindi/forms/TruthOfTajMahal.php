<li class="PointTutorials" >  ताजमहल कुरान के 14 अध्यायों से गुजर चुका है, लेकिन वहां कहीं भी नहीं है कि इस्लामिक ताज़ा के शाहजहां की ग्रन्थकारिता के लिए इस्लामिक ओवरराइटिंग में थोड़ी-थोड़ी सी बात है। यदि शाहजहां बिल्डर थे तो उन्होंने कुरान के उद्धरण के पहले इतने सारे शब्दों में ऐसा कहा होगा।
