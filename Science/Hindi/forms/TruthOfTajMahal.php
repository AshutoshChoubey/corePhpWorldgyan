<li class="PointTutorials" > . एक संस्कृत शिलालेख भी इस निष्कर्ष का समर्थन करता है कि ताज एक शिव मंदिर के रूप में उत्पन्न हुआ था। गलत रूप से बतेश्वर शिलालेख (वर्तमान में लखनऊ संग्रहालय की ऊपरी मंजिल पर संरक्षित) के रूप में कहा जाता है, यह एक "क्रिस्टल सफेद शिव मंदिर की स्थापना को इतना आकर्षक करता है कि भगवान शिव ने एक बार में निश्चय किया था कि वह अपने सामान्य निवास कैलाश पर्वत पर वापस कभी नहीं आए "। शाहीजहां के आदेशों पर ताजमहल बाग से 1155 ए.डी. का यह शिलालेख हटा दिया गया था। इतिहासकारों और आर्कियियोलॉजिस्टों ने बट्टेश्वर के शिलालेख को अंदाज़ा देने में चूक कर दी है, जब रिकॉर्ड यह नहीं कहता कि यह बैतस्वर द्वारा पाया गया था। वास्तव में, तेजोमहलया शिलालेख को बुलाया जाना चाहिए क्योंकि यह मूल रूप से ताज गार्डन में स्थापित किया गया था इससे पहले कि इसे ऊपर उठाना पड़ा और शाहजहां के आदेश पर निकाल दिया गया
</li><br/>
 
